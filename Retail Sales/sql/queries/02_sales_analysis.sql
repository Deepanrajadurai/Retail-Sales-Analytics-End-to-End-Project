use ecommers ;

--         Reusable View

CREATE OR REPLACE VIEW vw_sales AS
SELECT
    transaction_id,
    customer_id,
    age,
    gender,
    product_name,
    product_category,
    payment_mode,
    status,
    order_date,
    order_time,
    quantity,
    price,
    total_price,
    total_cost,
    (total_price - total_cost) AS profit
FROM sales_clean;

--      Indexes
CREATE INDEX idx_customer ON sales_clean(customer_id);
CREATE INDEX idx_product ON sales_clean(product_name);
CREATE INDEX idx_category ON sales_clean(product_category);
CREATE INDEX idx_order_date ON sales_clean(order_date);
CREATE INDEX idx_payment ON sales_clean(payment_mode);
CREATE INDEX idx_status ON sales_clean(status);

